package org.example.model;

/**
 * Перечисление типов файлов.
 *
 * Используется для классификации файлов по их основному назначению
 * или содержимому.
 */
public enum FileType {
    /**
     * Графические файлы изображений (JPEG, PNG, GIF и др.).
     */
    IMAGE,

    /**
     * Видео файлы (MP4, AVI, MOV и др.).
     */
    VIDEO,

    /**
     * Аудио файлы (MP3, WAV, FLAC и др.).
     */
    AUDIO,

    /**
     * Документы (PDF, DOC, DOCX, TXT и др.).
     */
    DOCUMENT,

    /**
     * Архивные файлы (ZIP, RAR, 7Z и др.).
     */
    ARCHIVE,

    /**
     * Исполняемые файлы (EXE, DLL и др.).
     */
    EXECUTABLE,

    /**
     * Неизвестный или не определенный тип файла.
     */
    UNKNOWN
}